char	*ft_strstr(const char *s, const char *to_find)
{
	int	i;
	int	j;

	i = 0;
	while (s[i] != '\0')
	{
		j = 0;
		while (to_find[j] == s[i + j])
		{
			if (to_find[i + j] == '\0')
				return (char*)(s + i);
			j++;
		}
		i++;
	}
	return (0);
}
